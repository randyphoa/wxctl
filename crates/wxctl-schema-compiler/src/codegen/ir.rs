// ---------------------------------------------------------------------------
// Descriptor IR — endpoint-fallback logic mirrors descriptor.rs:34-61.
// ---------------------------------------------------------------------------

/// `schema_static` is the already-emitted `SCHEMA_IR_<UPPER>` static's name —
/// the descriptor's `schema` field references it rather than re-emitting a
/// second copy of the (potentially large) `SchemaIr` literal.
fn emit_descriptor_ir(def: &ResourceDefinition, schema_static: &str) -> String {
    let create = def.api.create_endpoint.clone().unwrap_or_else(|| def.api.base_path.clone());
    let update = def.api.update_method.as_ref().map(|_| def.api.update_endpoint.clone().unwrap_or_else(|| def.api.get_endpoint.clone()));
    let delete = def.api.delete_endpoint.clone().unwrap_or_else(|| def.api.get_endpoint.clone());

    let endpoints = format!(
        "crate::ir::EndpointsIr {{ base_path: {}, list: {}, get: {}, create: {}, update: {}, update_method: {}, delete: {} }}",
        esc(&def.api.base_path),
        opt(&def.api.list_endpoint, |s| esc(s)),
        esc(&def.api.get_endpoint),
        esc(&create),
        opt(&update, |s| esc(s)),
        opt(&def.api.update_method, |m| http_method_ir(m).to_string()),
        esc(&delete),
    );

    let fields = slice(&def.schema.fields, |f: &FieldDefinition| format!("crate::ir::FieldDescriptorIr {{ name: {}, required: {}, immutable: {}, location: {} }}", esc(&f.name), f.required, f.immutable, field_location_ir(&f.location)));

    format!("crate::ir::DescriptorIr {{ name: {}, service: {}, kind: {}, id_field: {}, endpoints: {endpoints}, fields: {fields}, schema: &{schema_static} }}", esc(&def.name), esc(&def.service), esc(&def.kind), esc(&def.api.id_field),)
}
