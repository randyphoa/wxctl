//! Static IR codegen emitter.
//!
//! Turns the owned schema model (`crate::definition::*`) into Rust source text
//! for the `crate::ir::*` type family declared in `wxctl-schema/src/ir.rs`.
//! `generate_ir` is called once by `wxctl-schema/build.rs`; its output is
//! written to `OUT_DIR/schema_ir_generated.rs` and `include!`d from
//! `wxctl-schema/src/ir.rs`, so every `crate::ir::…` path below resolves inside
//! that crate's `ir` module once spliced in.
//!
//! All emitted values are const-constructible (`&'static str`, `&'static [..]`,
//! nested `&IrStruct { .. }` literals) — Rust's rvalue static promotion turns
//! each nested reference into a `'static` without an intermediate named static,
//! so one kind is one promoted top-level `static` literal.

use crate::build_meta::ParsedSchema;
use crate::definition::*;
use crate::deployment::Deployment;
use semver::Version;
use std::collections::HashMap;
use std::fmt::Write as _;

// ---------------------------------------------------------------------------
// Step 1: public entry point
// ---------------------------------------------------------------------------

/// Emit the static-IR Rust source for every schema in `schemas`, iterated over
/// `order` (topo order, index-aligned with `codegen::tables`).
pub fn generate_ir(order: &[String], schemas: &[ParsedSchema]) -> String {
    let by_name: HashMap<&str, &ParsedSchema> = schemas.iter().map(|p| (p.schema.resource.name.as_str(), p)).collect();

    let mut code = String::new();
    writeln!(code, "// Auto-generated by build.rs (via wxctl-schema-compiler::codegen::ir) - DO NOT EDIT").unwrap();
    writeln!(code).unwrap();

    // (kind, deployment-key, effective-static-path) for RESOURCE_IR_EFFECTIVE,
    // accumulated in the same topo order the per-kind statics are emitted.
    let mut effective_entries: Vec<(String, String, String)> = Vec::new();
    // (kind, static-path) pairs for the two phf lookups, built alongside the
    // per-kind statics so both passes iterate `order` identically.
    let mut resource_ir_entries: Vec<(String, String)> = Vec::new();
    let mut descriptor_ir_entries: Vec<(String, String)> = Vec::new();

    for name in order {
        let Some(parsed) = by_name.get(name.as_str()) else {
            continue;
        };
        let def = &parsed.schema.resource;
        let upper = ident_upper(&def.kind);
        let schema_static = format!("SCHEMA_IR_{upper}");
        let descriptor_static = format!("DESCRIPTOR_IR_{upper}");

        // 1. Base SchemaIr.
        writeln!(code, "pub static {schema_static}: crate::ir::SchemaIr = {};", emit_schema_ir(&parsed.schema)).unwrap();

        // 2. Per-deployment effective SchemaIr, one per declared overlay key
        // (only when the deployments map is non-empty — `deployments: {}` emits
        // no variant, the base applies everywhere).
        if let Some(deployments) = &def.deployments
            && !deployments.is_empty()
        {
            let mut keys: Vec<&String> = deployments.keys().collect();
            keys.sort();
            for key in keys {
                let deployment = deployment_for_key(key);
                let effective = crate::overlay::effective_definition(def, &deployment).unwrap_or_else(|e| panic!("effective_definition failed for kind '{}' deployment key '{key}': {e}", def.kind));
                let variant_static = format!("{schema_static}__{}", sanitize_key_ident(key));
                let effective_schema = ResourceSchema { resource: effective };
                writeln!(code, "pub static {variant_static}: crate::ir::SchemaIr = {};", emit_schema_ir(&effective_schema)).unwrap();
                effective_entries.push((def.kind.clone(), key.clone(), variant_static));
            }
        }

        // 3. DescriptorIr — endpoint-fallback logic from descriptor.rs:34-61.
        // `schema` references the base static declared just above rather than
        // re-emitting the (potentially large) SchemaIr literal a second time.
        writeln!(code, "pub static {descriptor_static}: crate::ir::DescriptorIr = {};", emit_descriptor_ir(def, &schema_static)).unwrap();
        writeln!(code).unwrap();

        resource_ir_entries.push((def.kind.clone(), format!("&{schema_static}")));
        descriptor_ir_entries.push((def.kind.clone(), format!("&{descriptor_static}")));
    }

    // 4. Lookups.
    let mut resource_ir_map = phf_codegen::Map::new();
    let resource_ir_entries: Vec<(&str, String)> = resource_ir_entries.iter().map(|(k, v)| (k.as_str(), v.clone())).collect();
    for (kind, path) in &resource_ir_entries {
        resource_ir_map.entry(*kind, path);
    }
    writeln!(code, "pub static RESOURCE_IR: phf::Map<&'static str, &'static crate::ir::SchemaIr> = {};", resource_ir_map.build()).unwrap();
    writeln!(code).unwrap();

    let mut descriptor_ir_map = phf_codegen::Map::new();
    let descriptor_ir_entries: Vec<(&str, String)> = descriptor_ir_entries.iter().map(|(k, v)| (k.as_str(), v.clone())).collect();
    for (kind, path) in &descriptor_ir_entries {
        descriptor_ir_map.entry(*kind, path);
    }
    writeln!(code, "pub static RESOURCE_DESCRIPTOR_IR: phf::Map<&'static str, &'static crate::ir::DescriptorIr> = {};", descriptor_ir_map.build()).unwrap();
    writeln!(code).unwrap();

    // NOTE: no inline `'static` on the tuple's reference fields (unlike the
    // `RESOURCE_IR`/`RESOURCE_DESCRIPTOR_IR` phf::Map statics above) — clippy's
    // `redundant_static_lifetimes` recurses into a slice-of-tuple static's type
    // (though not into opaque generic type params like `phf::Map<K, V>`), and
    // flags an explicit `'static` here as redundant since it's already implied.
    writeln!(code, "/// (kind, deployment-key, effective IR) for every non-empty `deployments` overlay.").unwrap();
    writeln!(code, "pub static RESOURCE_IR_EFFECTIVE: &[(&str, &str, &crate::ir::SchemaIr)] = &[").unwrap();
    for (kind, key, static_path) in &effective_entries {
        writeln!(code, "    ({}, {}, &{static_path}),", esc(kind), esc(key)).unwrap();
    }
    writeln!(code, "];").unwrap();

    code
}

// ---------------------------------------------------------------------------
// Deployment-key handling
// ---------------------------------------------------------------------------

/// Sanitize a `deployments:` overlay key into a valid Rust identifier fragment
/// (e.g. `software-5.3` -> `SOFTWARE_5_3`).
fn sanitize_key_ident(key: &str) -> String {
    key.to_uppercase().replace(['-', '.'], "_")
}

/// Mangle a resource `kind` into the uppercase fragment used in generated
/// static names (e.g. `database_connection` -> `DATABASE_CONNECTION`), mirroring
/// `build.rs`'s `mangle_name` convention.
fn ident_upper(kind: &str) -> String {
    kind.to_uppercase().replace('-', "_")
}

/// Construct a concrete `Deployment` that selects exactly the given overlay key
/// when run through `overlay::effective_definition`'s `select_overlay_key`.
/// Every overlay key in the current schema set is a bare `"saas"`/`"software"`
/// or a `"software-<version-prefix>"` form (e.g. `"software-5.3"`); this padds
/// the leading numeric components of the suffix to a full major.minor.patch
/// triple. Arbitrary range/operator constraints (`"software->=5.3, <6"`) are
/// not synthesized here — none occur in the schema set today.
fn deployment_for_key(key: &str) -> Deployment {
    if key == "saas" {
        return Deployment::Saas;
    }
    if key == "software" {
        return Deployment::Software { version: Version::new(0, 0, 0) };
    }
    let rest = key.strip_prefix("software-").unwrap_or_else(|| panic!("unrecognized deployment overlay key {key:?}: expected \"saas\", \"software\", or \"software-<version>\""));
    if let Ok(v) = Version::parse(rest) {
        return Deployment::Software { version: v };
    }
    let parts: Vec<u64> = rest.split('.').map(|p| p.trim_end_matches(['x', 'X', '*']).parse::<u64>().unwrap_or_else(|_| panic!("cannot derive a representative version for deployment overlay key {key:?}: non-numeric component {p:?}"))).collect();
    let major = parts.first().copied().unwrap_or(0);
    let minor = parts.get(1).copied().unwrap_or(0);
    let patch = parts.get(2).copied().unwrap_or(0);
    Deployment::Software { version: Version::new(major, minor, patch) }
}

// ---------------------------------------------------------------------------
// Step 2: literal-emission helpers (D2 untyped-JSON, D3 sorted maps)
// ---------------------------------------------------------------------------

// Untyped fields → canonical JSON string literal (D2). serde_json::Value serializes canonically;
// escape it into a Rust string literal with {:?}.
fn json_lit(v: &serde_json::Value) -> String {
    format!("{:?}", serde_json::to_string(v).expect("canonical json"))
}
// serde_norway::Value (prompt / overlay bodies) → serde_json::Value first, then json_lit.
fn yaml_to_json_lit(v: &serde_norway::Value) -> String {
    let j: serde_json::Value = serde_json::to_value(v).expect("yaml→json"); // structural, lossless for these shapes
    json_lit(&j)
}

/// Escape a plain string into a Rust string literal.
fn esc(s: &str) -> String {
    format!("{s:?}")
}

/// `Option<T>` → `Some(<f(x)>)` / `None`.
fn opt<T>(v: &Option<T>, f: impl Fn(&T) -> String) -> String {
    match v {
        Some(x) => format!("Some({})", f(x)),
        None => "None".to_string(),
    }
}

/// `&[T]` → `&[f(item), ...]`.
fn slice<T>(items: &[T], f: impl Fn(&T) -> String) -> String {
    let body: String = items.iter().map(|i| format!("{}, ", f(i))).collect();
    format!("&[{body}]")
}

/// `HashMap<String, V>` (D3) → `&[(key, f(val)), ...]` sorted by key.
fn sorted_pairs<V>(map: &HashMap<String, V>, f: impl Fn(&V) -> String) -> String {
    let mut keys: Vec<&String> = map.keys().collect();
    keys.sort();
    let body: String = keys.iter().map(|k| format!("({}, {}), ", esc(k), f(&map[k.as_str()]))).collect();
    format!("&[{body}]")
}

/// A `DeploymentOverlay` sub-block: `"null"` when absent (mirrors
/// `Value::is_null` skip_serializing_if), else its canonical JSON literal.
fn yaml_value_or_null_lit(v: &serde_norway::Value) -> String {
    if v.is_null() { esc("null") } else { yaml_to_json_lit(v) }
}

